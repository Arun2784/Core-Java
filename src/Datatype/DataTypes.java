package Datatype;

public class DataTypes {

	public static void main(String[] args) {

		// data type int
		int i = 10;
		int j = 20;
		int k = 25;

		System.out.println(j);

		// data type double.

		double d = 12.78;
		double d2 = 87;
		
		System.out.println(d2);
		
		
		//Char--Only single digit value can be written
		
		
		char c='a';
		char c1='b';
		char c3='c';
		
		System.out.println(c3);

		
		//String
		
		
		String s1="Arun";
		
		String s2="Naveen";
		
		
		System.out.println(s2);
	}

}
